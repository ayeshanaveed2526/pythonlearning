#Some Strings Functions
#String Length
name= "ayeshanaveed"
print(len(name))
#Strings ends with, function
print(name.endswith("eed"))
#tell if is it true or not
#Strings starts with, function
print(name.startswith("aya"))
#For capital letter
print(name.capitalize())
#for lower letter
print(name.lower())
#for upper letter
print(name.upper())
# for finding a string
print(name.find("sha"))
#for count any alphabet
print(name.count("a"))
#ESCAPE FUNCTION
#if we want to print new line in a string
print("Hello\nWorld")